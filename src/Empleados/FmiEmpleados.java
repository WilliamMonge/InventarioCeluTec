    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     Statement sl=null;
     ResultSet rs=null;
     if(txtCedula.getText().isEmpty()){
         JOptionPane.showMessageDialog(null, "Ingrese el Numero de Cedula", "Error", JOptionPane.WARNING_MESSAGE);
     }else if((txtCedula.getText().length()>10)){
            JOptionPane.showMessageDialog(null, "Dato demasiado largo", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
     try{
         sl=com.createStatement();
         rs=sl.executeQuery("SELECT * FROM WILLIAM.EMPLEADOS where cedula_emp='"+txtCedula.getText()+"'");
         rs.next();
         txtNombre.setText(rs.getString(2));
         cboEstadoCivil.setSelectedItem(rs.getString(3));
         if(rs.getString(4).toString().equals("Masculino")){
             rdbMaculino.setSelected(true);
             genero="Masculino";
         }else{
            rdbFemenino.setSelected(true);
             genero="Femenino";
                 }
         txtDirecion.setText(rs.getString(5));
         txtTelefCasa.setText(rs.getString(6));
         txtTelefCelular.setText(rs.getString(7));
         cboEdad.setSelectedItem(rs.getString(8));
         JOptionPane.showMessageDialog(null,"Busqueda Exitosa","Buscar",JOptionPane.INFORMATION_MESSAGE);
         } catch (SQLException ex) {
            //Logger.getLogger(FmiEmpleados.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ESTE DATO NO EXISTE EN LA BASE DE DATOS", "Error", JOptionPane.ERROR_MESSAGE);
        }
     }
    }//GEN-LAST:event_btnBuscarActionPerformed

