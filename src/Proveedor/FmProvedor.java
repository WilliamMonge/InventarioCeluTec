    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        Statement sl;
        if((txtRUC.getText().isEmpty())||(txtempresa.getText().isEmpty())||(txtNombre.getText().isEmpty())||(txtPagWeb.getText().isEmpty())||(txtTefono.getText().isEmpty())||(txtEmail.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "Ingresar Dato","Error", JOptionPane.WARNING_MESSAGE);
        }else if((txtRUC.getText().length()>13)||(txtNombre.getText().length()>70)||(txtempresa.getText().length()>70)||(txtPagWeb.getText().length()>70)||(txtTefono.getText().length()>10)||(txtEmail.getText().length()>70)){
            JOptionPane.showMessageDialog(null, "Dato demasiado largo", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
        try {
            sl = com.createStatement();
            sl.execute("Insert into WILLIAM.PROVEEDOR values('" + txtRUC.getText().toString() + "','"
                + txtempresa.getText().toString() + "','"
                + txtNombre.getText().toString() + "','"
                + cboProvincia.getSelectedItem() + "','"
                + txtPagWeb.getText().toString() + "','"
                + txtTefono.getText().toString() + "','"
                + txtEmail.getText().toString() + "','"
                +Fechaderegistro3()+"')");
            JOptionPane.showMessageDialog(null, "Elemento Guradado con exito", "Guardar", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            //Logger.getLogger(FmProvedor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ESTE DATO YA EXISTE EN LA BASE DE DATOS", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

