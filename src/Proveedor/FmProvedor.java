    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Statement sl=null;
        ResultSet rs=null;
        if(txtRUC.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar el RUC","Error", JOptionPane.WARNING_MESSAGE);
        }else if((txtRUC.getText().length()>13)){
            JOptionPane.showMessageDialog(null, "Dato demasiado largo", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
        try{
            sl=com.createStatement();
            rs=sl.executeQuery("SELECT * FROM WILLIAM.PROVEEDOR where RUC_prov='"+txtRUC.getText()+"'");
            rs.next();
            txtempresa.setText(rs.getString(2));
            txtNombre.setText(rs.getString(3));
            cboProvincia.setSelectedItem(rs.getString(4));
            txtPagWeb.setText(rs.getString(5));
            txtTefono.setText(rs.getString(6));
            txtEmail.setText(rs.getString(7));
        } catch (SQLException ex) {
            Logger.getLogger(FmiEmpleados.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ESTE ELEMENTO NO EXISTE EN LA BASE DE DATOS", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

